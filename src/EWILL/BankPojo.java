package EWILL;

public class BankPojo {
	int bankId;
	int userId;
	String firstHolder;
	String bankName;
	String bankAdd;
	String accntNo;
	String nominee;
	public int getBankId() {
		return bankId;
	}
	public void setBankId(int bankId) {
		this.bankId = bankId;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getFirstHolder() {
		return firstHolder;
	}
	public void setFirstHolder(String firstHolder) {
		this.firstHolder = firstHolder;
	}
	public String getBankName() {
		return bankName;
	}
	public void setBankName(String bankName) {
		this.bankName = bankName;
	}
	public String getBankAdd() {
		return bankAdd;
	}
	public void setBankAdd(String bankAdd) {
		this.bankAdd = bankAdd;
	}
	public String getAccntNo() {
		return accntNo;
	}
	public void setAccntNo(String accntNo) {
		this.accntNo = accntNo;
	}
	public String getNominee() {
		return nominee;
	}
	public void setNominee(String nominee) {
		this.nominee = nominee;
	}
	
	
}
