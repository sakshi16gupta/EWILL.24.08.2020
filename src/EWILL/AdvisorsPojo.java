package EWILL;

public class AdvisorsPojo {
		int advisorsId;
		int userId;
		String name;
		String address;
		String contact;
		String email;
		int advisorsType;
		
		public int getAdvisorsId() {
			return advisorsId;
		}
		public void setAdvisorsId(int advisorsId) {
			this.advisorsId = advisorsId;
		}
		public int getUserId() {
			return userId;
		}
		public void setUserId(int userId) {
			this.userId = userId;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getAddress() {
			return address;
		}
		public void setAddress(String address) {
			this.address = address;
		}
		public String getContact() {
			return contact;
		}
		public void setContact(String contact) {
			this.contact = contact;
		}
		public String getEmail() {
			return email;
		}
		public void setEmail(String email) {
			this.email = email;
		}
		public int getAdvisorsType() {
			return advisorsType;
		}
		public void setAdvisorsType(int advisorsType) {
			this.advisorsType = advisorsType;
		}
		
}
